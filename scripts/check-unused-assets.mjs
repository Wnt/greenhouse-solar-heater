#!/usr/bin/env node
// Checks that every asset in playground/assets/** and every non-vendored
// file in playground/public/ is referenced somewhere in the codebase.
//
// See docs/superpowers/specs/2026-04-22-tech-debt-dead-code-and-file-size-design.md §5.3
//
// Flags:
//   --strict   exit 1 if any asset has zero references. Default: warn mode.

import { readFileSync } from 'node:fs';
import { basename } from 'node:path';
import { execSync } from 'node:child_process';

const STRICT = process.argv.includes('--strict');

// Vendored third-party files in playground/public/ — intentionally present
// even if greppable references don't exist. Keep in sync with CLAUDE.md
// "Vendored dependencies must stay vendored".
const VENDORED = new Set([
  'playground/public/simplewebauthn-browser.mjs',
  'playground/public/qrcode-generator.mjs',
  'playground/public/material-symbols.css',
  'playground/public/material-symbols-outlined.woff2',
]);

// Files generated by scripts/, validated by drift tests; excluded from this
// check because they live in the assets dir for shipping but their lifecycle
// is owned by the generator script.
const GENERATED = new Set([
  'playground/assets/bootstrap-history.json',
  'playground/assets/system-topology.drawio',
  'playground/assets/system-topology.svg',
  'playground/assets/liquid-glass-displacement.png',
  'playground/assets/liquid-glass-specular.png',
  'playground/assets/liquid-glass.json',
]);

function listCandidates() {
  const out = execSync('git ls-files playground/assets playground/public', { encoding: 'utf8' });
  return out.split('\n').filter(Boolean)
    .filter(p => !VENDORED.has(p))
    .filter(p => !GENERATED.has(p));
}

function listSearchCorpus() {
  // Every committed file under which a reference could plausibly live.
  const out = execSync(
    "git ls-files '*.html' '*.js' '*.mjs' '*.cjs' '*.css' '*.json' '*.webmanifest' '*.md' '*.yaml' '*.yml' '*.ts'",
    { encoding: 'utf8' }
  );
  return out.split('\n').filter(Boolean);
}

function hasReference(assetPath, corpus) {
  const name = basename(assetPath);
  for (const src of corpus) {
    // Don't count the asset itself as a reference.
    if (src === assetPath) continue;
    const content = readFileSync(src, 'utf8');
    if (content.includes(name)) return src;
  }
  return null;
}

const candidates = listCandidates();
const corpus = listSearchCorpus();

const unused = [];
for (const asset of candidates) {
  if (!hasReference(asset, corpus)) unused.push(asset);
}

if (unused.length === 0) {
  console.log(`check-unused-assets: all ${candidates.length} assets referenced ✓`);
  process.exit(0);
}

for (const asset of unused) {
  console.log(`✗ ${asset}  (no references found)`);
}
console.log('');
console.log(`check-unused-assets: ${unused.length} unreferenced of ${candidates.length} checked`);

if (STRICT) {
  process.exit(1);
}
console.log('(warn mode — not failing. Pass --strict to enforce.)');
process.exit(0);
